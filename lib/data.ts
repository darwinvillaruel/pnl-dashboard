export const REGIONS = ["Australia", "UK", "US", "New Zealand", "Canada"] as const;
export type Region = typeof REGIONS[number];

export const MONTHS = [
  "January", "February", "March", "April", "May", "June",
  "July", "August", "September", "October", "November", "December",
] as const;

const MONTH_ABBR = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];
const DAY_NAMES = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];

export function generateDaysForMonth(month: number, year: number) {
  const daysInMonth = new Date(year, month + 1, 0).getDate();
  const abbr = MONTH_ABBR[month];
  return Array.from({ length: daysInMonth }, (_, i) => {
    const day = i + 1;
    return {
      key: `${day}- ${abbr}`,
      label: `${abbr} ${day}`,
      day: DAY_NAMES[new Date(year, month, day).getDay()],
    };
  });
}

export function getVerticalsByRegion(region: Region): VerticalData[] {
  if (region === "Australia") return RAW_VERTICALS;
  return [];
}

export function getVerticalsByPlatform(platform: "Facebook" | "TikTok" | "All"): VerticalData[] {
  if (platform === "All") return RAW_VERTICALS;
  return RAW_VERTICALS.filter((v) => v.platform === platform);
}

export function getActiveVerticals(verticals: VerticalData[]): VerticalData[] {
  return verticals.filter((v) => v.totalLeads > 0);
}

export function getSummaryStats(verticals: VerticalData[]) {
  const active = getActiveVerticals(verticals);
  const totalLeads = active.reduce((s, v) => s + v.totalLeads, 0);
  // Lead-weighted averages so high-volume verticals aren't drowned out by small ones
  const avgROI = totalLeads > 0
    ? active.reduce((s, v) => s + v.avgROI * v.totalLeads, 0) / totalLeads
    : 0;
  const avgMargin = totalLeads > 0
    ? active.reduce((s, v) => s + v.avgMargin * v.totalLeads, 0) / totalLeads
    : 0;
  return { totalLeads, avgROI, avgMargin, activeVerticals: active.length };
}

export function getDailyAggregated(verticals: VerticalData[], days = DAYS) {
  return days.map((d) => {
    const vals = verticals.map((v) => v.daily[d.key]).filter((x) => x && x.leadsSold > 0);
    const leads = vals.reduce((s, x) => s + x.leadsSold, 0);
    const avgROI = vals.length > 0 ? vals.reduce((s, x) => s + x.roi, 0) / vals.length : 0;
    const avgMargin = vals.length > 0 ? vals.reduce((s, x) => s + x.margin, 0) / vals.length : 0;
    return { ...d, leads, avgROI, avgMargin };
  });
}
